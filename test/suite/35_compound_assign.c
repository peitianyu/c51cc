/* 35_compound_assign: 复合赋值运算符 */

int main(void) {
    int a = 10;
    a += 5;     /* 15 */
    a -= 3;     /* 12 */
    a *= 2;     /* 24 */
    a /= 3;     /* 8 */
    a %= 5;     /* 3 */
    a <<= 2;    /* 12 */
    a >>= 1;    /* 6 */
    a &= 0xF;   /* 6 */
    a |= 0x10;  /* 22 */
    a ^= 0x3;   /* 21 */
    return a;    /* 21 */
}
